export type RecipeTag = 'anticonstipatie' | 'dulce' | 'low-budget' | 'proteic' | 'legume' | 'cereale' | 'fructe' | 'peste' | 'fibre-mari'

export function getRecipeTags(recipe: { id: string; name: string; ingredients: string[]; mealType: string }): RecipeTag[] {
  const tags: RecipeTag[] = []
  const ingredientsStr = recipe.ingredients.join(' ').toLowerCase()
  const nameStr = recipe.name.toLowerCase()

  // Anticonstipatie - alimente bogate in fibre
  const antiConst = ['pruna', 'para', 'broccoli', 'ovaz', 'fulgi', 'linte', 'fasole', 'sfecla', 'piersica', 'zmeura', 'afine', 'seminte chia', 'quinoa', 'spanac']
  if (antiConst.some(a => ingredientsStr.includes(a))) tags.push('anticonstipatie')

  // Dulce
  const dulce = ['banana', 'mar', 'para', 'mango', 'capsuni', 'afine', 'zmeura', 'curmale', 'miere', 'vanilie']
  const dulceCount = dulce.filter(d => ingredientsStr.includes(d)).length
  if (dulceCount >= 2 || nameStr.includes('dulce') || nameStr.includes('budinca') || nameStr.includes('clatite') || nameStr.includes('briosă') || nameStr.includes('granola')) tags.push('dulce')

  // Low budget
  const expensive = ['somon', 'cod', 'biban', 'miel', 'ricotta', 'mozzarella', 'quinoa', 'amarant', 'tahini', 'unt de migdale', 'unt de arahide']
  if (!expensive.some(e => ingredientsStr.includes(e))) tags.push('low-budget')

  // Proteic
  const proteine = ['pui', 'vita', 'curcan', 'somon', 'cod', 'biban', 'ou', 'linte', 'fasole', 'naut', 'tofu', 'branza', 'iaurt']
  if (proteine.filter(p => ingredientsStr.includes(p)).length >= 1) tags.push('proteic')

  // Legume
  const legume = ['morcov', 'cartof', 'dovleac', 'dovlecel', 'broccoli', 'conopida', 'spanac', 'mazare', 'rosie', 'ardei', 'sfecla', 'telina', 'pastarnac']
  if (legume.filter(l => ingredientsStr.includes(l)).length >= 1) tags.push('legume')

  // Cereale
  const cereale = ['orez', 'ovaz', 'fulgi', 'paste', 'paine', 'faina', 'gris', 'malai', 'quinoa', 'hrisca', 'mei', 'amarant', 'taitei', 'toast', 'tortilla', 'cuscus', 'lasagna', 'pesmet', 'grissini']
  if (cereale.filter(c => ingredientsStr.includes(c)).length >= 1) tags.push('cereale')

  // Fructe
  const fructe = ['banana', 'mar', 'para', 'mango', 'capsuni', 'afine', 'zmeura', 'piersica', 'portocala', 'ananas', 'pruna']
  if (fructe.filter(f => ingredientsStr.includes(f)).length >= 1) tags.push('fructe')

  // Peste
  if (['somon', 'cod', 'biban', 'merlucius', 'pangasius'].some(p => ingredientsStr.includes(p))) tags.push('peste')

  // Fibre mari
  if (tags.includes('anticonstipatie') && (ingredientsStr.includes('linte') || ingredientsStr.includes('fasole') || ingredientsStr.includes('pruna') || ingredientsStr.includes('seminte chia'))) tags.push('fibre-mari')

  return tags
}

export function generateSmartWeekPlan(
  recipes: any[],
  ageGroup: string,
  meals: string[],
  weekSeed: number,
  mode: 'normal' | 'anticonstipatie' = 'normal'
): Record<string, Record<string, any>> {
  const days = ['Luni', 'Marți', 'Miercuri', 'Joi', 'Vineri', 'Sâmbătă', 'Duminică']
  const plan: Record<string, Record<string, any>> = {}

  // Filter recipes by age (ex: "4+", "8+" → număr corect)
  const ageMatch = String(ageGroup).match(/(\d+)/)
  const ageNum = ageMatch ? Number(ageMatch[1]) : 12
  const eligible = recipes.filter(r => {
    const m = String(r.ageGroup).match(/(\d+)/)
    const rAge = m ? Number(m[1]) : 99
    return rAge <= ageNum
  })

  // Track used ingredients per week to avoid repetition
  const usedIngredients: string[] = []
  const usedRecipeIds: string[] = []
  const sweetCount = { count: 0 }

  days.forEach((day, dayIndex) => {
    plan[day] = {}
    meals.forEach((meal, mealIndex) => {
      const mealRecipes = eligible.filter(r => r.mealType === meal)
      if (mealRecipes.length === 0) return

      let candidates = mealRecipes

      if (mode === 'anticonstipatie') {
        // Prefer anticonstipatie recipes
        const antiCandidates = mealRecipes.filter(r => {
          const tags = getRecipeTags(r)
          return tags.includes('anticonstipatie') || tags.includes('fibre-mari')
        })
        if (antiCandidates.length >= 2) candidates = antiCandidates
      }

      // Avoid too many sweet recipes (max 2 per day)
      if (sweetCount.count >= 2) {
        const nonSweet = candidates.filter(r => !getRecipeTags(r).includes('dulce'))
        if (nonSweet.length > 0) candidates = nonSweet
      }

      // Prefer recipes with different ingredients than used ones
      const fresh = candidates.filter(r =>
        !r.ingredients.some((ing: string) =>
          usedIngredients.some(used => ing.toLowerCase().includes(used))
        )
      )
      if (fresh.length > 0) candidates = fresh

      const notUsed = candidates.filter(r => !usedRecipeIds.includes(r.id))
      if (notUsed.length > 0) candidates = notUsed

      // Seeded random selection
      const seed = weekSeed * 100 + dayIndex * 10 + mealIndex
      const selected = candidates[seed % candidates.length]

      plan[day][meal] = selected
      if (selected?.id) usedRecipeIds.push(selected.id)

      // Track used main ingredients
      if (selected?.ingredients) {
        const mainIngs = selected.ingredients.slice(0, 2).map((i: string) =>
          i.toLowerCase().replace(/[0-9]/g, '').trim().split(' ')[0]
        )
        usedIngredients.push(...mainIngs)
        if (getRecipeTags(selected).includes('dulce')) sweetCount.count++
      }
    })
  })

  return plan
}

export function analyzeWeekBalance(plan: Record<string, Record<string, any>>): {
  proteic: number
  legume: number
  cereale: number
  dulce: number
  lowBudget: number
  total: number
  tooSweet: boolean
} {
  let proteic = 0, legume = 0, cereale = 0, dulce = 0, lowBudget = 0, total = 0

  Object.values(plan).forEach(dayPlan => {
    Object.values(dayPlan).forEach((recipe: any) => {
      if (!recipe) return
      total++
      const tags = getRecipeTags(recipe)
      if (tags.includes('proteic')) proteic++
      if (tags.includes('legume')) legume++
      if (tags.includes('cereale')) cereale++
      if (tags.includes('dulce')) dulce++
      if (tags.includes('low-budget')) lowBudget++
    })
  })

  return { proteic, legume, cereale, dulce, lowBudget, total, tooSweet: dulce > total * 0.35 }
}
