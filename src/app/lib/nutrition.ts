export function estimateRecipeNutrition(ingredients: string[], ageGroup: string) {
  let totalKcal = 0, totalProtein = 0, totalFat = 0, totalCarbs = 0, totalFiber = 0

  ingredients.forEach(ingredient => {
    const lower = ingredient.toLowerCase()

    Object.entries(ingredientNutrition).forEach(([key, values]) => {
      if (lower.includes(key)) {
        // Use realistic small portions for baby (30-50g per ingredient)
        const factor = 0.35
        totalKcal += values.kcal * factor
        totalProtein += values.protein * factor
        totalFat += values.fat * factor
        totalCarbs += values.carbs * factor
        totalFiber += values.fiber * factor
      }
    })
  })

  // Baby gets a small portion - divide by number of ingredients to avoid stacking
  const ingredientCount = Math.max(ingredients.length, 1)
  const babyFactor = 1 / ingredientCount

  return {
    kcal: Math.round(totalKcal * babyFactor),
    protein: Math.round(totalProtein * babyFactor * 10) / 10,
    fat: Math.round(totalFat * babyFactor * 10) / 10,
    carbs: Math.round(totalCarbs * babyFactor * 10) / 10,
    fiber: Math.round(totalFiber * babyFactor * 10) / 10,
    dailyNeeds: babyDailyNeeds[ageGroup] || babyDailyNeeds['8+'],
  }
}

